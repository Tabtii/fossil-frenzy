// Helper functions
export function getCardById(id: string): Card | undefined {
  return allCards.find(card => card.id === id);
}

export function getCardsByRarity(rarity: string): Card[] {
  return allCards.filter(card => card.rarity === rarity);
}

export function getCardsByType(type: string): Card[] {
  return allCards.filter(card => card.type === type);
}

export function getCardsByEra(era: string): Card[] {
  return allCards.filter(card => card.era === era);
}

export function upgradeCard(card: Card): Card {
  if (!card.upgradeable) return card;
  
  const upgraded: Card = {
    ...card,
    upgraded: true,
    name: card.name + '+',
  };
  
  if (card.upgradeDamage !== undefined && upgraded.damage !== undefined) {
    upgraded.damage = card.damage! + card.upgradeDamage;
  }
  if (card.upgradeBlock !== undefined && upgraded.block !== undefined) {
    upgraded.block = card.block! + card.upgradeBlock;
  }
  if (card.upgradeEffect) {
    upgraded.effect = card.upgradeEffect;
  }
  
  return upgraded;
}
